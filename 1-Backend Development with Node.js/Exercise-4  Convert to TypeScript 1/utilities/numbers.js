// multiply

const multiply = (num1, num2) => {
    return num1 * num2;
};

// add
const sum = (num1, num2) => {
    return num1 + num2;
};

// divide
const divide = (num1, num2) => {
    return num1 / num2;
};

// subtract
const subtract = (num1, num2) => {
    return num1 - num2;
};

// square
const square = (num) => {
    return num * num;
};

module.exports = {
    multiply,
    subtract,
    divide,
    sum,
    square
  };